package com.qa;

public class EvenAndOddPrint {

	public static void main(String[] args) {
		System.out.println("Even Number.........");
		for(int i=0; i<=100 ; i++) {
			
				if (i%2==0) {
					
					System.out.println(i);
					
				}
			
		}
		System.out.println("odd number.....");
		for(int j=0;j<=100;j++) {
			
		     if(j%2!=0) {
			   
			   System.out.println(j);
		    }
		}
	}
	}

		
